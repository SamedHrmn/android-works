package com.samedharman.calculator_basic

class Calculator{

    fun sum(num1: Int,num2: Int):Int{
        return num1 + num2
    }

    fun minus(num1: Int,num2: Int):Int{
        return num1 - num2
    }

    fun divide(num1: Float,num2: Float):Float{
        return num1 / num2
    }

    fun multiply(num1: Int,num2: Int):Int{
        return num1 * num2
    }

}